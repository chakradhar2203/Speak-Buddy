export interface Language {
  code: string;
  name: string;
  nativeName: string;
  bcp47: string;
}

export interface SuggestedReply {
  id: string;
  textInUserLang: string;
  textInListenerLang: string;
  emoji: string;
}

export interface Scenario {
  id: string;
  name: string;
  emoji: string;
  description: string;
  replies: Record<string, SuggestedReply[]>; // keyed by `${userLang}-${listenerLang}`
  templatePhrases?: TemplatePhrase[];
}

export interface FillOption {
  translations: { [langCode: string]: string };
}

export interface TemplatePlaceholder {
  key: string;
  label: string; // English label shown to user
  options: FillOption[];
}

export interface TemplatePhrase {
  id: string;
  emoji: string;
  template: { [langCode: string]: string }; // Text with {key} placeholder
  placeholders: TemplatePlaceholder[];
}

