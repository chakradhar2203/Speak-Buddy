// =============================================
// Dynamic reply builder — generates SuggestedReply[]
// for ANY language pair from the phrase entries
// =============================================

function buildRepliesForAllPairs(phrases: PhraseEntry[]): Record<string, SuggestedReply[]> {
  const result: Record<string, SuggestedReply[]> = {};
  const langCodes = SUPPORTED_LANGUAGES.map((l) => l.code);

  for (const userCode of langCodes) {
    for (const listenerCode of langCodes) {
      if (userCode === listenerCode) continue;
      const key = langPairKey(userCode, listenerCode);
      result[key] = phrases
        .filter((p) => p.translations[userCode] && p.translations[listenerCode])
        .map((p) => ({
          id: p.id,
          textInUserLang: p.translations[userCode],
          textInListenerLang: p.translations[listenerCode],
          emoji: p.emoji,
        }));
    }
  }

  return result;
}

