// Get replies for a specific scenario and language pair
export function getReplies(
  scenarioId: string,
  userLangCode: string,
  listenerLangCode: string
): SuggestedReply[] {
  const scenario = SCENARIOS.find((s) => s.id === scenarioId);
  if (!scenario) return [];
  
  const key = langPairKey(userLangCode, listenerLangCode);
  const replies = scenario.replies[key];
  
  if (replies && replies.length > 0) return replies;
  
  // Fallback: Try en-hi or hi-en as default
  return scenario.replies['en-hi'] || scenario.replies['hi-en'] || [];
}

export function getTemplatePhrases(scenarioId: string): TemplatePhrase[] {
  const scenario = SCENARIOS.find((s) => s.id === scenarioId);
  return scenario?.templatePhrases || [];
}

export function buildTemplatePhrase(
  template: TemplatePhrase,
  selections: Record<string, FillOption>,
  langCode: string
): { userText: string; listenerText: string } {
  let userText = template.template[langCode] || template.template['en'] || '';
  
  for (const [key, option] of Object.entries(selections)) {
    const value = option.translations[langCode] || option.translations['en'] || '';
    userText = userText.replace(new RegExp(`\\{${key}\\}`, 'g'), value);
  }
  
  return { userText, listenerText: userText };
}

export function resolveTemplate(
  template: TemplatePhrase,
  selections: Record<string, FillOption>,
  userLangCode: string,
  listenerLangCode: string
): { textInUserLang: string; textInListenerLang: string } {
  let textInUserLang = template.template[userLangCode] || template.template['en'] || '';
  let textInListenerLang = template.template[listenerLangCode] || template.template['en'] || '';
  
  for (const [key, option] of Object.entries(selections)) {
    const userVal = option.translations[userLangCode] || option.translations['en'] || '';
    const listenerVal = option.translations[listenerLangCode] || option.translations['en'] || '';
    textInUserLang = textInUserLang.replace(new RegExp(`\\{${key}\\}`, 'g'), userVal);
    textInListenerLang = textInListenerLang.replace(new RegExp(`\\{${key}\\}`, 'g'), listenerVal);
  }
  
  return { textInUserLang, textInListenerLang };
}
